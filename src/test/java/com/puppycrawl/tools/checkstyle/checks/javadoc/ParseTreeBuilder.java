    /*
     * +--javadoc
     *   |
     *   +--htmlElement
     *     |
     *     +--nonSpecialHtmlElement
     *       |
     *       +--htmlElementOpen
     *         |
     *         +--OPEN
     *         +--NAME
     *         +--CLOSE
     *       +--misc
     *         |
     *         +--text
     *           |
     *           +--CHAR
     *           + ...
     *           +--CHAR
     *       +--htmlElementClose
     *         |
     *         +--OPEN
     *         +--SLASH
     *         +--NAME
     *         +--CLOSE
     */
    public static ParseTree treeOneSimpleHtmlTag()
    {
        JavadocContext tree = new JavadocContext(null, -1);

        HtmlElementContext htmlElement = new HtmlElementContext(tree, 1);
        HtmlTagContext nonSpecialHtmlElement = new HtmlTagContext(
                htmlElement, 1);
        HtmlElementOpenContext htmlElementOpen = new HtmlElementOpenContext(
                htmlElement, 1);
        MiscContext misc = new MiscContext(htmlElement, 1);
        HtmlElementCloseContext htmlElementClose = new HtmlElementCloseContext(
                htmlElement, 1);
        TextContext text = new TextContext(misc, 1);

        CommonToken blabla = new CommonToken(JavadocTokenTypes.CHAR,
                "b l a b l a");
        CommonToken open = new CommonToken(JavadocTokenTypes.OPEN, "<");
        CommonToken slash = new CommonToken(JavadocTokenTypes.SLASH, "/");
        CommonToken close = new CommonToken(JavadocTokenTypes.CLOSE, ">");
        CommonToken g = new CommonToken(JavadocTokenTypes.HTML_TAG_IDENT, "g");
        CommonToken EOF = new CommonToken(Token.EOF, "<EOF>");

        tree.addChild(htmlElement);

        htmlElement.addChild(nonSpecialHtmlElement);

        nonSpecialHtmlElement.addChild(htmlElementOpen);
        nonSpecialHtmlElement.addChild(misc);
        nonSpecialHtmlElement.addChild(htmlElementClose);

        htmlElementOpen.addChild(open);
        htmlElementOpen.addChild(g);
        htmlElementOpen.addChild(close);

        misc.addChild(text);
        text.addChild(blabla);

        htmlElementClose.addChild(open);
        htmlElementClose.addChild(slash);
        htmlElementClose.addChild(g);
        htmlElementClose.addChild(close);

        tree.addChild(EOF);

        return tree;
    }

